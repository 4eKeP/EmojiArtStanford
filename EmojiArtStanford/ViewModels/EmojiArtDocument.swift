            self.autosave()
        }
    }
    private struct Autosave {
        static let filename = "Autosave.emojiart"
        static var url: URL? {
            // –Ω–∞ ios –≤—Å–µ–≥–¥–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—å—Å—è .userDomainMask –¥—Ä—É–≥–∏–µ –¥–µ—Ä–∏–∫—Ç–æ—Ä–∏–∏ –∫–∞–∫ –ø—Ä–∞–≤–∏–ª–æ –¥–ª—è –º–∞–∫
